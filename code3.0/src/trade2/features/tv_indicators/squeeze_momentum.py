    def _lag1(arr: np.ndarray) -> np.ndarray:
        out_arr = np.empty_like(arr)
        out_arr[0] = arr[0]
        out_arr[1:] = arr[:-1]
        return out_arr
    
    # Bull/bear: momentum direction + squeeze released
    bull_raw = (~squeeze_on) & (momentum_raw > 0)
