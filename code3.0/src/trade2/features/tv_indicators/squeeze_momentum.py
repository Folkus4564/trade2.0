    kc_length = params.get('kc_length', 20)
    kc_mult = params.get('kc_mult', 1.5)
    
    out = df.copy()
    close = out["Close"].astype(float).values
    high = out["High"].astype(float).values
    low = out["Low"].astype(float).values
    
    # Calculate Bollinger Bands
    bb_middle = talib.SMA(close, timeperiod=bb_length)
    bb_std = talib.STDDEV(close, timeperiod=bb_length)
    bb_upper = bb_middle + 2 * bb_std
    bb_lower = bb_middle - 2 * bb_std
    
    # Calculate Keltner Channels
    typical_price = (high + low + close) / 3.0
    kc_middle = talib.SMA(typical_price, timeperiod=kc_length)
    
    atr = talib.ATR(high, low, close, timeperiod=kc_length)
    kc_upper = kc_middle + kc_mult * atr
    kc_lower = kc_middle - kc_mult * atr
    
    # Squeeze detection
    squeeze_on = (bb_upper < kc_upper) & (bb_lower > kc_lower)
    
