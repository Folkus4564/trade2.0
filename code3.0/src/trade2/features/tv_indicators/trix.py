    period = trix_cfg.get("period", 18)
    signal_period = trix_cfg.get("signal", 9)
    
    out = df.copy()
    close = out["Close"].astype(float).values
    
    # Triple EMA calculation
    ema1 = talib.EMA(close, timeperiod=period)
    ema2 = talib.EMA(ema1, timeperiod=period)
    ema3 = talib.EMA(ema2, timeperiod=period)
    
    # TRIX = percent rate of change of triple EMA
    trix_line = np.zeros_like(ema3)
    trix_line[1:] = ((ema3[1:] - ema3[:-1]) / ema3[:-1]) * 100
    
    # Signal line (EMA of TRIX)
    signal_line = talib.EMA(trix_line, timeperiod=signal_period)
    
    # Boolean conditions (before shift)
    trix_bull_raw = trix_line > signal_line
    trix_bear_raw = trix_line < signal_line
    
    # Helper: numpy-level 1-bar lag
    def _lag1(arr: np.ndarray) -> np.ndarray:
        out_arr = np.empty_like(arr)
        out_arr[0] = False
        out_arr[1:] = arr[:-1]
        return out_arr
    
    # Shift(1) all outputs for lag safety
    idx = out.index
    out["trix_line"] = pd.Series(trix_line, index=idx).shift(1)
    out["trix_signal"] = pd.Series(signal_line, index=idx).shift(1)
