import numpy as np
import pandas as pd
import talib
from typing import Dict, Any


def add_trix_features(df: pd.DataFrame, config: Dict[str, Any]) -> pd.DataFrame:
    """
    Compute TRIX (Triple Exponential Average) momentum indicator.
    
    Formula:
      1. EMA1 = EMA(Close, period)
      2. EMA2 = EMA(EMA1, period)
      3. EMA3 = EMA(EMA2, period)
      4. TRIX = ((EMA3 - EMA3[1]) / EMA3[1]) * 100
      5. Signal = EMA(TRIX, signal_period)
    
    Signals:
      trix_bull: TRIX > Signal (momentum positive)
      trix_bear: TRIX < Signal (momentum negative)
    
    Args:
        df: OHLCV DataFrame
        config: Full config dict. Reads config["tv_indicators"]["trix"].
    
    Returns:
        df copy with TRIX columns added (all shift(1) for lag safety).
    """
