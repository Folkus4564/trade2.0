    out = df.copy()
    close = out["Close"].astype(float).values
    volume = out["Volume"].astype(float).values

    # Calculate raw OBV using TA-Lib
    obv_raw = talib.OBV(close, volume)

    # Smooth OBV if period > 1
    if smooth_period > 1:
        obv_smooth = talib.EMA(obv_raw, timeperiod=smooth_period)
    else:
        obv_smooth = obv_raw.copy()

    # Create boolean signals for rising/falling OBV
    obv_diff = np.zeros_like(obv_smooth)
    obv_diff[1:] = obv_smooth[1:] - obv_smooth[:-1]

    obv_bull_raw = obv_diff > 0
    obv_bear_raw = obv_diff < 0

    # Helper: numpy-level 1-bar lag
    def _lag1(arr: np.ndarray) -> np.ndarray:
        out_arr = np.empty_like(arr)
        out_arr[0] = False
        out_arr[1:] = arr[:-1]
        return out_arr

