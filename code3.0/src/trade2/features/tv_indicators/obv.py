    out["obv_smooth"] = pd.Series(obv_smooth, index=idx).shift(1)
    out["obv_bull"] = pd.Series(_lag1(obv_bull_raw), index=idx, dtype=bool)
    out["obv_bear"] = pd.Series(_lag1(obv_bear_raw), index=idx, dtype=bool)

    return out