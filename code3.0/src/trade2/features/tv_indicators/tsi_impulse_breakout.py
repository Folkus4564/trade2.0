    prev_highest = np.empty(n)
    prev_highest[0] = np.nan
    prev_highest[1:] = highest_high[:-1]

    prev_lowest = np.empty(n)
    prev_lowest[0] = np.nan
    prev_lowest[1:] = lowest_low[:-1]

    price_breakout_up   = close > prev_highest
    price_breakout_down = close < prev_lowest

