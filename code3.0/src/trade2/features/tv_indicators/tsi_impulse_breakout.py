    long_period   = int(params.get('long_period',   8))
    short_period  = int(params.get('short_period',  4))
    signal_period = int(params.get('signal_period', 3))

    # Clamp to scalping-safe ranges
    long_period   = max(3, min(long_period,   14))
    short_period  = max(3, min(short_period,  10))
    signal_period = max(2, min(signal_period,  5))

    out   = df.copy()
    close = out["Close"].astype(float).values
    n     = len(close)

    # --- True Strength Index (TSI) ---
    # 1) Price change
    pc = np.empty(n)
    pc[0] = 0.0
    pc[1:] = close[1:] - close[:-1]

    # 2) Double-smoothed momentum: EMA(EMA(pc, long), short)
    ema1_pc = talib.EMA(pc,        timeperiod=long_period)
    ema2_pc = talib.EMA(ema1_pc,   timeperiod=short_period)

    # 3) Double-smoothed absolute momentum: EMA(EMA(|pc|, long), short)
    abs_pc  = np.abs(pc)
    ema1_abs = talib.EMA(abs_pc,   timeperiod=long_period)
    ema2_abs = talib.EMA(ema1_abs, timeperiod=short_period)

    # 4) TSI = 100 * double_smooth_pc / double_smooth_abs_pc
    tsi = np.where(ema2_abs != 0.0, 100.0 * ema2_pc / ema2_abs, 0.0)

    # 5) Signal line: EMA of TSI
    signal = talib.EMA(tsi, timeperiod=signal_period)

    # --- Breakout / Impulse detection (raw, pre-shift) ---
    # Bull: TSI crosses above signal AND TSI > 0
    # Bear: TSI crosses below signal AND TSI < 0

    tsi_above_signal = tsi > signal
    tsi_below_signal = tsi < signal

    def _lag1(arr: np.ndarray) -> np.ndarray:
        result = np.empty(len(arr), dtype=bool)
        result[0] = False
        result[1:] = arr[:-1]
        return result

    # Cross above: currently above, previously below or equal
    cross_above = tsi_above_signal & ~_lag1(tsi_above_signal)
    # Cross below: currently below, previously above or equal
    cross_below = tsi_below_signal & ~_lag1(tsi_below_signal)

    bull_raw = cross_above & (tsi > 0)
    bear_raw = cross_below & (tsi < 0)

    # --- Directional breakout confirmation via price channel ---
    # Bullish breakout: close > highest high of last lookback bars (breakout up)
    # Bearish breakout: close < lowest low  of last lookback bars (breakout down)
