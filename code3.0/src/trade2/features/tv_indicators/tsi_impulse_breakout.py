def add_tsi_impulse_breakout_features(df: pd.DataFrame, config: Dict[str, Any]) -> pd.DataFrame:
    """
    Compute TSI Impulse Breakout features for scalping on 5M charts.

    True Strength Index (TSI) measures momentum using double-smoothed price changes.
    This implementation uses short periods suitable for scalping.

    Zones / Signals:
      tsi_impulse_breakout_bull: TSI crosses above signal line AND TSI > 0 (bullish momentum)
      tsi_impulse_breakout_bear: TSI crosses below signal line AND TSI < 0 (bearish momentum)

    All outputs are shift(1) for lag safety.

    Args:
        df:     OHLCV DataFrame
        config: Full config dict. Reads config["tv_indicators"]["tsi_impulse_breakout"].

    Returns:
        df copy with tsi_impulse_breakout_ columns added.
    """
